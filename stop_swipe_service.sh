#!/bin/bash

# Скрипт для остановки микросервиса свайпов
echo "Остановка микросервиса свайпов..."

# Находим и убиваем процесс на порту 5002
PID=$(lsof -ti:5002)
if [ ! -z "$PID" ]; then
    kill $PID
    echo "Микросервис свайпов остановлен (PID: $PID)"
else
    echo "Микросервис свайпов не запущен"
fi
